package ud.susel.util;

public class SuselMetadataNullException extends Exception {
    public SuselMetadataNullException() {
        super();
    }

    public SuselMetadataNullException(String message) {
        super(message);
    }

    public SuselMetadataNullException(String message, Throwable cause) {
        super(message, cause);
    }

    public SuselMetadataNullException(Throwable cause) {
        super(cause);
    }

    public SuselMetadataNullException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}

