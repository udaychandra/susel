package ud.susel.util;

public class SuselPropertiesNullException extends Exception {
    public SuselPropertiesNullException() {
        super();
    }

    public SuselPropertiesNullException(String message) {
        super(message);
    }

    public SuselPropertiesNullException(String message, Throwable cause) {
        super(message, cause);
    }

    public SuselPropertiesNullException(Throwable cause) {
        super(cause);
    }

    public SuselPropertiesNullException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}

